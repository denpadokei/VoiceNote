using System;
using System.Linq;
using UnityEngine;
using VoiceNote.Configuration;
using Zenject;

namespace VoiceNote
{
    public class AudioVolumeGetter : MonoBehaviour, IInitializable
    {
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プロパティ
        public float AudioLevel { get; private set; }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド用メソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // オーバーライドメソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // パブリックメソッド
        public void Initialize()
        {
            this._deviceName = PluginConfig.Instance.MicrophoneDeviceName;

            this._audioClip = Microphone.Start(this._deviceName, true, 10, 48000);
            this._ampGain = PluginConfig.Instance.Gain;
        }

        public void Update()
        {
            this.GetUpdatedAudio(out this._waveData);
            if (this._waveData.Length == 0) {
                return;
            }
            this._audioLevel = this._waveData.Average(Mathf.Abs);
            var now = Time.frameCount % s_frameCount;
            this._aveLevel[now] = this._ampGain * this._audioLevel;
            this.AudioLevel = this._aveLevel.Max();
        }

        /// <summary>
        /// 負荷やばそう
        /// </summary>
        /// <param name="waveData"></param>
        public void GetUpdatedAudio(out float[] waveData)
        {
            var nowAudioPos = Microphone.GetPosition(this._deviceName);

            if (this._lastAudioPos < nowAudioPos) {
                var audioCount = nowAudioPos - this._lastAudioPos;
                waveData = new float[audioCount];
                this._audioClip.GetData(waveData, this._lastAudioPos);
            }
            else if (this._lastAudioPos > nowAudioPos) {
                var audioBuffer = this._audioClip.samples * this._audioClip.channels;
                var audioCount = audioBuffer - this._lastAudioPos;

                var wave1 = new float[audioCount];
                this._audioClip.GetData(wave1, this._lastAudioPos);

                var wave2 = new float[nowAudioPos];
                if (nowAudioPos != 0) {
                    this._audioClip.GetData(wave2, 0);
                }

                waveData = new float[audioCount + nowAudioPos];
                wave1.CopyTo(waveData, 0);
                wave2.CopyTo(waveData, audioCount);
            }
            else {
                waveData = Array.Empty<float>();
            }

            this._lastAudioPos = nowAudioPos;
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プライベートメソッド

        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // メンバ変数
        private string _deviceName;
        private AudioClip _audioClip;
        private int _lastAudioPos;
        private float _audioLevel;
        private float _ampGain = 10;
        private readonly float[] _aveLevel = new float[s_frameCount];
        private const int s_frameCount = 10;
        private float[] _waveData;
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // 構築・破棄
        public void OnDestroy()
        {
            Microphone.End(this._deviceName);
            if (this._audioClip != null) {
                Destroy(this._audioClip);
            }
        }
        #endregion
    }
}
